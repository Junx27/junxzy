package commands

import (
	"fmt"
	"os"
	"path/filepath"

	"github.com/Junx27/junxzy/internal/ui"
)

type InitCommand struct{}

func (i InitCommand) Name() string {
	return "init"
}

func (i InitCommand) Execute(args []string) {
	if len(args) < 1 {
		ui.Error("Nama project wajib diisi")
		return
	}

	projectName := args[0]

	// ✅ Validasi: jangan overwrite project
	if _, err := os.Stat(projectName); !os.IsNotExist(err) {
		ui.Error("Project sudah ada: " + projectName)
		return
	}

	// root project
	ui.Step("Membuat folder project...")
	createDir(projectName)

	base := projectName

	// services
	ui.Step("Membuat services...")
	services := []string{"user", "auth", "product"}

	for _, svc := range services {
		createService(base, svc)
		ui.Success("service " + svc + " dibuat")
	}

	// gateway
	ui.Step("Membuat gateway...")
	os.MkdirAll(filepath.Join(base, "gateway"), os.ModePerm)

	// docker-compose
	ui.Step("Membuat docker-compose...")
	createDockerCompose(base)
	ui.Success("docker-compose dibuat")

	// readme
	ui.Step("Membuat README...")
	createReadme(base, projectName)
	ui.Success("README dibuat")

	fmt.Println()
	ui.Success("Project microservices berhasil dibuat: " + projectName)
}

// ================= HELPER =================

func createDir(path string) {
	os.MkdirAll(path, os.ModePerm)
}

func createService(base, name string) {
	servicePath := filepath.Join(base, "services", name)

	dirs := []string{
		"handler",
		"service",
		"repository",
	}

	for _, d := range dirs {
		os.MkdirAll(filepath.Join(servicePath, d), os.ModePerm)
	}

	// main.go sederhana
	mainFile := `package main

import "fmt"

func main() {
	fmt.Println("` + name + ` service running")
}
`

	os.WriteFile(filepath.Join(servicePath, "main.go"), []byte(mainFile), os.ModePerm)
}

func createDockerCompose(base string) {
	content := `version: '3'

services:
  user:
    build: ./services/user
    ports:
      - "8001:8000"

  auth:
    build: ./services/auth
    ports:
      - "8002:8000"

  product:
    build: ./services/product
    ports:
      - "8003:8000"
`

	os.WriteFile(filepath.Join(base, "docker-compose.yml"), []byte(content), os.ModePerm)
}

func createReadme(base, name string) {
	content := "# " + name + `

Microservices project generated by Junxzy CLI 🚀
`

	os.WriteFile(filepath.Join(base, "README.md"), []byte(content), os.ModePerm)
}
