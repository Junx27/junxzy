package commands

import (
	"fmt"
	"os"
	"path/filepath"

	"github.com/Junx27/junxzy/internal/ui"
)

type InitCommand struct{}

func (i InitCommand) Name() string {
	return "init"
}

func (i InitCommand) Execute(args []string) {
	if len(args) < 1 {
		ui.StopError("Nama project wajib diisi")
		return
	}

	projectName := args[0]

	if _, err := os.Stat(projectName); !os.IsNotExist(err) {
		ui.StopError("Project sudah ada: " + projectName)
		return
	}

	ui.Start("Membuat folder project...")
	if err := createDir(projectName); err != nil {
		ui.StopError(err.Error())
		return
	}
	ui.StopSuccess("Folder project dibuat")

	base := projectName

	services := []string{"user", "auth", "product"}

	ui.Start("Membuat services...")
	for _, svc := range services {
		if err := createService(base, svc); err != nil {
			ui.StopError(err.Error())
			return
		}
	}
	ui.StopSuccess("Semua services berhasil dibuat")

	ui.Start("Membuat gateway...")
	if err := os.MkdirAll(filepath.Join(base, "gateway"), os.ModePerm); err != nil {
		ui.StopError(err.Error())
		return
	}
	ui.StopSuccess("Gateway dibuat")

	ui.Start("Membuat docker-compose...")
	if err := createDockerCompose(base); err != nil {
		ui.StopError(err.Error())
		return
	}
	ui.StopSuccess("docker-compose dibuat")

	ui.Start("Membuat README...")
	if err := createReadme(base, projectName); err != nil {
		ui.StopError(err.Error())
		return
	}
	ui.StopSuccess("README dibuat")

	fmt.Println()
	ui.Success("Project microservices berhasil dibuat: " + projectName)
}

// ================= HELPER =================

func createDir(path string) error {
	return os.MkdirAll(path, os.ModePerm)
}

func createService(base, name string) error {
	servicePath := filepath.Join(base, "services", name)

	dirs := []string{
		"handler",
		"service",
		"repository",
	}

	for _, d := range dirs {
		if err := os.MkdirAll(filepath.Join(servicePath, d), os.ModePerm); err != nil {
			return err
		}
	}

	// main.go sederhana
	mainFile := `package main

import "fmt"

func main() {
	fmt.Println("` + name + ` service running")
}
`

	return os.WriteFile(filepath.Join(servicePath, "main.go"), []byte(mainFile), os.ModePerm)
}

func createDockerCompose(base string) error {
	content := `version: '3'

services:
  user:
    build: ./services/user
    ports:
      - "8001:8000"

  auth:
    build: ./services/auth
    ports:
      - "8002:8000"

  product:
    build: ./services/product
    ports:
      - "8003:8000"
`

	return os.WriteFile(filepath.Join(base, "docker-compose.yml"), []byte(content), os.ModePerm)
}

func createReadme(base, name string) error {
	content := "# " + name + `

Microservices project generated by Junxzy CLI 🚀
`

	return os.WriteFile(filepath.Join(base, "README.md"), []byte(content), os.ModePerm)
}
